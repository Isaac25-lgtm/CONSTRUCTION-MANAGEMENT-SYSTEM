interface ItemRow { id: number | string; description: string; unit: string; qty: string; price: string; total: string }

function ItemsPanel({ items, showPrice, priceLabel, qtyLabel, onAdd, onDelete }: {
  items: ItemRow[]
  showPrice: boolean
  priceLabel?: string
  qtyLabel?: string
  onAdd?: (d: ItemPayload) => Promise<void>
  onDelete?: (itemId: number) => Promise<void>
}) {
  const [adding, setAdding] = useState(false)
  const [desc, setDesc] = useState('')
  const [unit, setUnit] = useState('unit')
  const [qty, setQty] = useState('')
  const [price, setPrice] = useState('')
  const { showToast } = useUIStore()

  return (
    <div className="border-t border-bp-border bg-[#0d1526] px-4 py-3" onClick={(e) => e.stopPropagation()}>
      <div className="mb-2 flex items-center justify-between">
        <span className="text-[11px] font-semibold uppercase tracking-wider text-bp-muted">Line Items ({items.length})</span>
        {onAdd && !adding && (
          <ActionButton variant="blue" size="sm" onClick={() => setAdding(true)}>+ Add Item</ActionButton>
        )}
      </div>
      {items.length > 0 ? (
        <table className="w-full text-xs">
          <thead>
            <tr className="text-left text-bp-muted">
              <th className="pb-1 pr-2">#</th>
              <th className="pb-1 pr-2">Description</th>
              <th className="pb-1 pr-2 w-[60px]">Unit</th>
              <th className="pb-1 pr-2 w-[70px]">{qtyLabel || 'Qty'}</th>
              {showPrice && <th className="pb-1 pr-2 w-[90px]">{priceLabel || 'Unit Price'}</th>}
              {showPrice && <th className="pb-1 pr-2 w-[100px]">Total</th>}
              {onDelete && <th className="pb-1 w-[40px]"></th>}
            </tr>
          </thead>
          <tbody>
            {items.map((item, idx) => (
              <tr key={item.id} className="border-t border-bp-border/50">
                <td className="py-1 pr-2 text-bp-muted">{idx + 1}</td>
                <td className="py-1 pr-2 text-bp-text">{item.description}</td>
                <td className="py-1 pr-2 text-bp-muted">{item.unit}</td>
                <td className="py-1 pr-2 font-mono text-bp-info">{item.qty}</td>
                {showPrice && <td className="py-1 pr-2 font-mono text-bp-muted">{item.price}</td>}
                {showPrice && <td className="py-1 pr-2 font-mono font-semibold text-bp-warning">{item.total ? formatUGX(parseFloat(item.total)) : '-'}</td>}
                {onDelete && (
                  <td className="py-1">
                    <button className="text-bp-muted hover:text-bp-danger text-[10px]" onClick={() => onDelete(Number(item.id))}>&#10005;</button>
                  </td>
                )}
              </tr>
            ))}
          </tbody>
        </table>
      ) : (
        <p className="text-[11px] text-bp-muted">No line items yet.</p>
      )}
      {adding && (
        <div className="mt-2 grid grid-cols-[1fr_60px_70px_auto] gap-1.5 items-end">
          <input className="text-xs" placeholder="Description" value={desc} onChange={(e) => setDesc(e.target.value)} autoFocus />
          <input className="text-xs" placeholder="Unit" value={unit} onChange={(e) => setUnit(e.target.value)} />
          <input className="text-xs" type="number" placeholder="Qty" value={qty} onChange={(e) => setQty(e.target.value)} />
          {showPrice && <input className="text-xs w-[80px]" type="number" placeholder="Price" value={price} onChange={(e) => setPrice(e.target.value)} />}
          <div className="flex gap-1">
            <ActionButton variant="green" size="sm" onClick={async () => {
              if (!desc || !qty) { showToast('Description and quantity required', 'warning'); return }
              const payload: Record<string, unknown> = { description: desc, unit, quantity: parseFloat(qty) }
              if (showPrice && price) payload.unit_price = parseFloat(price)
              if (!showPrice && price) payload.received_quantity = parseFloat(qty)
              await onAdd!(payload)
              setDesc(''); setQty(''); setPrice(''); setAdding(false)
            }}>Save</ActionButton>
            <ActionButton variant="ghost" size="sm" onClick={() => setAdding(false)}>Cancel</ActionButton>
          </div>
        </div>
      )}
    </div>
  )
}

