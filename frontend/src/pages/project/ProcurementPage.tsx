function AddPaymentModal({ projectId, onClose }: { projectId: string; onClose: () => void }) {
  const [supplier, setSupplier] = useState('')
  const [amount, setAmount] = useState('')
  const [paymentDate, setPaymentDate] = useState(new Date().toISOString().slice(0, 10))
  const [method, setMethod] = useState('')
  const { data: suppliers, isLoading: loadingSuppliers } = useSuppliers()
  const create = useCreatePayment(projectId)
  const { showToast } = useUIStore()

  return (
    <Modal open={true} onClose={onClose} title="Record Payment" width={420}>
      <div className="grid gap-3">
        <div>
          <label className="mb-1 block text-xs font-semibold text-bp-muted">Supplier *</label>
          {loadingSuppliers ? (
            <select disabled><option>Loading suppliers...</option></select>
          ) : (
            <select value={supplier} onChange={(e) => setSupplier(e.target.value)}>
              <option value="">-- Select supplier --</option>
              {(suppliers || []).map(s => <option key={s.id} value={s.id}>{s.name}</option>)}
            </select>
          )}
        </div>
        <div>
          <label className="mb-1 block text-xs font-semibold text-bp-muted">Amount (UGX) *</label>
          <input type="number" value={amount} onChange={(e) => setAmount(e.target.value)} placeholder="0" />
        </div>
        <div className="grid grid-cols-2 gap-3">
          <div>
            <label className="mb-1 block text-xs font-semibold text-bp-muted">Payment Date *</label>
            <input type="date" value={paymentDate} onChange={(e) => setPaymentDate(e.target.value)} />
          </div>
          <div>
            <label className="mb-1 block text-xs font-semibold text-bp-muted">Method</label>
            <select value={method} onChange={(e) => setMethod(e.target.value)}>
              <option value="">-- Select --</option>
              <option value="bank_transfer">Bank Transfer</option>
              <option value="mobile_money">Mobile Money</option>
              <option value="cheque">Cheque</option>
              <option value="cash">Cash</option>
            </select>
          </div>
        </div>
        <ActionButton variant="green" className="!w-full !mt-1" onClick={async () => {
          if (!supplier) { showToast('Please select a supplier', 'warning'); return }
          if (!amount) { showToast('Amount is required', 'warning'); return }
          await create.mutateAsync({
            supplier,
            amount: parseFloat(amount),
            payment_date: paymentDate,
            method: method || undefined,
          })
          showToast('Payment recorded', 'success'); onClose()
        }} disabled={create.isPending}>{create.isPending ? 'Recording...' : 'Record Payment'}</ActionButton>
      </div>
    </Modal>
  )
}
