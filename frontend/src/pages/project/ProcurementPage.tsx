import { useEffect, useState, type ReactNode } from 'react'
import {
  PageHeader, CostCard, DataTable, StatusBadge, ActionButton,
  Modal, LoadingState, EmptyState, Tabs,
} from '../../components/ui'
import {
  usePurchaseOrders, useProcurementSummary, useCreatePO,
  useSuppliers, useRFQs, useCreateRFQ,
  useQuotations, useCreateQuotation,
  useGoodsReceipts, useCreateGRN,
  useProcurementInvoices, useCreateInvoice,
  useProcurementPayments, useCreatePayment,
  type SupplierData, type PurchaseOrderData, type RFQData,
  type QuotationData, type GoodsReceiptData,
  type ProcurementInvoiceData, type ProcurementPaymentData,
} from '../../hooks/useProcurement'
